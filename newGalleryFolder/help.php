
<h3>How use newGalleryFolder on your theme?</h3>


<p> The code will show photos from the root directory <br> <b>example:</b> &#60;?php showGalleryFolderRoot('300px','200px') ;?&#62; </p>

<code style="width:100%;border:solid 1px;display:block;padding:10px;">&#60;?php showGalleryFolderRoot($width,$height) ;?&#62;</code>

<br>

<p> the code will show you a folder with a specific name <br> <b>example:</b> &#60;?php showGalleryFolder('yourfoldername','300px','200px') ;?&#62; </p>

<code style="width:100%;border:solid 1px;display:block;padding:10px;">&#60;?php showGalleryFolder($filefolder,$width,$height) ;?&#62;</code>

<br>

<p> the code will show you a folder with a specific name (reverse order) <br> <b>example:</b> &#60;?php  showGalleryFolderReverse('yourfoldername','300px','200px') ;?&#62;</p>

<code style="width:100%;border:solid 1px;display:block;padding:10px;">&#60;?php showGalleryFolderReverse($filefolder,$width,$height) ;?&#62;</code>
<br>

<p> the code will show photos from a specific folder only on a given subpage <br> <b>example:</b>  &#60;?php showGalleryFolderOnPage('yourfoldername','slug','300px','200px') ;?&#62;</p>

<code style="width:100%;border:solid 1px;display:block;padding:10px;">&#60;?php showGalleryFolderOnPage($filefolder,$pageslug,$width,$height) ;?&#62;</code>


<br>

<p> the code will show photos from a specific folder only on a given subpage (reverse order)  <br> <b>example:</b>  &#60;?php showGalleryFolderOnPageReverse('yourfoldername','slug','300px','200px') ;?&#62;</p>

<code style="width:100%;border:solid 1px;display:block;padding:10px;">&#60;?php showGalleryFolderOnPageReverse($filefolder,$pageslug,$width,$height) ;?&#62;</code>


